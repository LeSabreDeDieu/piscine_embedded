#include <avr/io.h>

#define N 2000000 // (16000000(16MHz) * 0.5) / 4 (car 4 instruction CPU par boucle)

int main( void ) {
	DDRB |= (1 << PB1);
    PORTB &= ~(1 << PB1);

	while (1) {
		uint32_t i = N + 1;
		while (--i);
		PORTB ^= (1 << PB1);
	}
}