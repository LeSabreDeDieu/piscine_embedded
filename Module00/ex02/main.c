#include <avr/io.h>

int main(void) {
    DDRB |= (1 << PB0);
    PORTB &= ~(1 << PB0);

    DDRD &= ~(1 << PIND2);
    PORTD |= (1 << PIND2);
    while (1) {
        if (!(PIND & (1 << PIND2))) {
            PORTB |= (1 << PB0);
        } else {
            PORTB &= ~(1 << PB0);
        }
    }
    return 0;
}